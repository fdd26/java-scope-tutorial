package same;

public class Parent
{
	public Parent()
	{
		super();
	}

	public         int x;
	protected      int y;
	/* package */  int z;
	private        int w;

	public int getX()
	{
		return this.x;
	}

	public int getY()
	{
		return this.y;
	}

	public int getZ()
	{
		return this.z;
	}

	public int getW()
	{
		return this.w;
	}
}
